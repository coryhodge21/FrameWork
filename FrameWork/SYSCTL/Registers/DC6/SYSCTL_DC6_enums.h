typedef enum { 

	USB0_OTG	=	0x00000003,	// USB0 is OTG
	USB0_HOSTDEV	=	0x00000002,	// USB is Device or Host
	USB0_DEV	=	0x00000001,	// USB0 is Device Only
	USB0_M	=	0x00000003,	// USB Module 0 Present
	USB0PHY	=	0x00000010,	// USB Module 0 PHY Present

} DC6_e;
